CREATE TABLE [dbo].[Asset] (
    [asset_id]                INT            IDENTITY (1, 1) NOT NULL,
    [asset_code]              NVARCHAR (100) NOT NULL,
    [asset_name]              NVARCHAR (200) NOT NULL,
    [asset_description]       NVARCHAR (256) NULL,
    [asset_level]             VARCHAR (100)  NOT NULL,
    [site_code]               NVARCHAR (100) NOT NULL,
    [parent_asset_code]       NVARCHAR (100) NOT NULL CONSTRAINT [DF_Asset_parent_asset_code] DEFAULT (' '),
    [value_stream]            NVARCHAR (100) NULL,
    [automation_level]        VARCHAR (100)  NULL,
    [include_in_escalation]   BIT            NULL,
    [grouping1]               NVARCHAR (256) NULL,
    [grouping2]               NVARCHAR (256) NULL,
    [grouping3]               NVARCHAR (256) NULL,
    [grouping4]               NVARCHAR (256) NULL,
    [grouping5]               NVARCHAR (256) NULL,
    [status]                  VARCHAR (50)   NOT NULL,
    [entered_by]              NVARCHAR (100)  NOT NULL CONSTRAINT [DF_Asset_Entered_By] DEFAULT (N'SQL Manual Entry'),
    [entered_on]              DATETIME NOT NULL CONSTRAINT [DF_Asset_Entered_On] DEFAULT (getdate()),
    [last_modified_by]        NVARCHAR (100) NOT NULL CONSTRAINT [DF_Asset_last_modified_by] DEFAULT (N'SQL Manual Entry'),
    [last_modified_on]        DATETIME       NOT NULL CONSTRAINT [DF_Asset_last_modified_On] DEFAULT (getdate()),
    [target_percent_of_ideal] FLOAT (53)     NULL,
    [is_multiple]             BIT NOT NULL CONSTRAINT [DF_Asset_is_multiple] DEFAULT (0),
    [is_dynamic] BIT NOT NULL DEFAULT (0), 
    CONSTRAINT [PK_Asset_Asset_Id] PRIMARY KEY CLUSTERED ([asset_id] ASC),
    CONSTRAINT [UNC_Asset_Asset_Code] UNIQUE NONCLUSTERED ([asset_code] ASC)
);

